package org.netbeans.cubeon.bugzilla.api.model;

/**
 * Contains basic information about bug, it will be used to represent
 * bug's list elements (rows).
 *
 * @author radoslaw.holewa
 */
public class BugSummary {

    /**
     * Bug id.
     */
    private Integer id;
    /**
     * Bug's short description.
     */
    private String summary;
    /**
     * Bug serverity.
     */
    private String severity;
    /**
     * Bug priority.
     */
    private String priority;
    /**
     * Bug operating system.
     */
    private String operatingSystem;
    /**
     * Bug asignee.
     */
    private String assignee;
    /**
     * Bug status.
     */
    private String status;
    /**
     * Bug resolution.
     */
    private String resolution;

    public Integer getId() {
        return id;
    }

    public void setId(Integer id) {
        this.id = id;
    }

    public String getSummary() {
        return summary;
    }

    public void setSummary(String summary) {
        this.summary = summary;
    }

    public String getSeverity() {
        return severity;
    }

    public void setSeverity(String severity) {
        this.severity = severity;
    }

    public String getPriority() {
        return priority;
    }

    public void setPriority(String priority) {
        this.priority = priority;
    }

    public String getOperatingSystem() {
        return operatingSystem;
    }

    public void setOperatingSystem(String operatingSystem) {
        this.operatingSystem = operatingSystem;
    }

    public String getAssignee() {
        return assignee;
    }

    public void setAssignee(String assignee) {
        this.assignee = assignee;
    }

    public String getStatus() {
        return status;
    }

    public void setStatus(String status) {
        this.status = status;
    }

    public String getResolution() {
        return resolution;
    }

    public void setResolution(String resolution) {
        this.resolution = resolution;
    }
}
